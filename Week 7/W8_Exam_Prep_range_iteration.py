"""An iterable object containing sequential integers.

Author: Akaash Deo"""

range(0, 10)                                    # Creates a range object for numbers from 0 to 9

# Iterate by steps of 2
range(0, 10, 2)                                 # Creates a range object for numbers from 0 to 9, stepping by 2

# Print an example
print(range(0, 10, 2))                         # Outputs the range object representing numbers [0, 2, 4, 6, 8]
