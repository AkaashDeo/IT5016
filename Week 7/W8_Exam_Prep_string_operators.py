s = 'Ryan ' + 'Mitchell'                         # Combines the two strings 'Ryan ' and 'Mitchell'
print(s)                                         # Outputs: Ryan Mitchell

# String multiplication
multiplication = 'Python' * 6                    # Repeats the string 'Python' six times
print(multiplication)                             # Outputs: PythonPythonPythonPythonPythonPython

# Access a particular character in a string
character = 'Python'[3]                          # Retrieves the character at index 3 (zero-based), which is 'h'
print(character)                                  # Outputs: h
