x = 1                               # Assigning an integer value to x
type(x)                             # Checking the type of x (integer)
print(type(x))                     # Outputting the type of x (should be <class 'int'>)

# Floats
x = 1.0                             # Assigning a float value to x
type(x)                             # Checking the type of x (float)
print(type(x))                     # Outputting the type of x (should be <class 'float'>)

# Strings
x = 'Ryan'                          # Assigning a string value to x
type(x)                             # Checking the type of x (string)
print(type(x))                     # Outputting the type of x (should be <class 'str'>)

# Booleans
x = True                            # Assigning a boolean value to x (True)
type(x)                             # Checking the type of x (boolean)
print(type(x))                     # Outputting the type of x (should be <class 'bool'>)

# Byte data
x = bytes(4)                       # Creating a bytes object of length 4
type(x)                             # Checking the type of x (bytes)
print(type(x))                     # Outputting the type of x (should be <class 'bytes'>)
