
"""
Program to calculate the lengths of different strings.

This program demonstrates how to use the `len()` function to determine 
the number of characters in various string variables and outputs the results.

Author: Akaash Deo
"""

# INPUT
x = 'a'                    # Initialize a string variable 'x' with a single character 'a'.
x1 = "abc"                 # Initialize a string variable 'x1' with the value 'abc'.
x2 = "Hello world"         # Initialize a string variable 'x2' with the value 'Hello world'.
# PROCESS
len1 = len(x)              # Calculate the length of string 'x' and store it in 'len1'.
len2 = len(x1)             # Calculate the length of string 'x1' and store it in 'len2'.
len3 = len(x2)             # Calculate the length of string 'x2' and store it in 'len3'.
# OUTPUT
print(len1, len2, len3)    # Print the lengths of the three strings: len1, len2, and len3.
